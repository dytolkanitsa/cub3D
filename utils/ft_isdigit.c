#include "../includes/cub3D.h"

int	ft_isdigit(int c)
{
	if (c >= 48 && c <= 57)
		return (1);
	else
		return (0);
}

int	ft_isdigit_fixed(int c)
{
	if ((c >= 48 && c <= 57) || c == ' ')
		return (1);
	else
		return (0);
}
